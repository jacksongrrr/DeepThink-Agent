def format_classification_block(profile: dict[str, str]) -> str:
    """将分类 JSON 格式化为路径生成器可读的纯文本块。"""
    order = (
        "domain_type",
        "difficulty",
        "subcategory",
        "structure_type",
        "thinking_stance",
    )
    lines: list[str] = []
    for k in order:
        v = (profile.get(k) or "").strip()
        if v:
            lines.append(f"- **{k}**：{v}")
    for k, v in sorted(profile.items()):
        if k in order:
            continue
        vv = str(v).strip()
        if vv:
            lines.append(f"- **{k}**：{vv}")
    return "\n".join(lines) if lines else "（无分类信息）"


def format_synthesis_traces_block(path_runs: list[dict[str, str | None]]) -> str:
    """将各路径的 R1 推理与小结格式化为综合阶段的上下文。"""
    lines: list[str] = []
    for i, run in enumerate(path_runs, start=1):
        p = (run.get("path") or "").strip()
        d = (run.get("detail") or "").strip()
        reasoning = (run.get("reasoning") or "").strip() or "（无显式推理文本）"
        ans = (run.get("answer") or "").strip() or "（无）"
        lines.append(f"### 路径 {i}：{p}")
        lines.append("#### 路径展开（Chat）")
        lines.append(d)
        lines.append("#### R1 推理过程")
        lines.append(reasoning)
        lines.append("#### R1 该路径小结")
        lines.append(ans)
        lines.append("")
    return "\n".join(lines).strip() or "（无）"
