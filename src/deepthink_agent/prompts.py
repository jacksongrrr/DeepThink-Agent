def format_synthesis_traces_block(path_runs: list[dict[str, str | None]]) -> str:
    """将各路径的 R1 推理与小结格式化为综合阶段的上下文。"""
    lines: list[str] = []
    for i, run in enumerate(path_runs, start=1):
        p = (run.get("path") or "").strip()
        d = (run.get("detail") or "").strip()
        reasoning = (run.get("reasoning") or "").strip() or "（无显式推理文本）"
        ans = (run.get("answer") or "").strip() or "（无）"
        lines.append(f"### 路径 {i}：{p}")
        lines.append("#### 路径展开（Chat）")
        lines.append(d)
        lines.append("#### R1 推理过程")
        lines.append(reasoning)
        lines.append("#### R1 该路径小结")
        lines.append(ans)
        lines.append("")
    return "\n".join(lines).strip() or "（无）"
